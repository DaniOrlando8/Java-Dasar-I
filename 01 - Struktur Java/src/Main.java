/* 
    Setiap file Java mempunyai class. 
    Di dalam class, ada method. 
*/
public class Main {
    public static void main(String[] args) {
        System.out.println("Hello, Struktur!"); //Java membutuhkan titik koma ; pada akhir tiap statement.  
    }                                           //Jika tidak diberi titik koma, Anda akan mendapat error.
}                                               //Jadi jangan sampai lupa titik koma ;
/*

Pada contoh di bawah, kita punya class yang namanya structur, dan method main. 
    
    (Class)
    public class struktur {
        (Method)
        public static void main(String[] args) {
        System.out.println("Hello, Struktur!");
    }   (Method)
}   (Class)

*/