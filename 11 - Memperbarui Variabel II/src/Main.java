/* ~ Memperbarui Variabel II ~
 Untuk memperbarui variable dengan penambahan, 
 Anda hanya perlu menentukan jumlah nilai variable saat ini dengan sebuah angka untuk di tambahkan kembali ke variable tersebut,
 Lihat Script dibawah untuk lebih jelas. 
 Ini mungkin terlihat aneh, namun ingat bahwa = dalam Java artinya menentukan, bukan sama dengan.
*/
public class Main {
    public static void main(String[] args) {
        int x = 1;
        System.out.println(x);

        x = x + 2;
     // x Ditambahkan 1
        System.out.println(x);
    }
}
