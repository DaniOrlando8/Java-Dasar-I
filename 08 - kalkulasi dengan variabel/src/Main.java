/* ~ Kalkulasi dengan Variabel ~

    Jika variable adalah sebuah integer, Anda dapat melakukan kalkulasi dengannya,
    sama seperti dengan nilai integer biasa.
    Anda dapat menggunakannya dengan nilai integer lainnya, atau variable integer.
*/
public class Main {
    public static void main(String[] args) {
        int number1 = 10;
        System.out.println(number1 + 10);
    //------------------------------------//
        int number2 = 5;
        System.out.println(number1 + number2);
    }
}
