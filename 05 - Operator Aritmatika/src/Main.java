/* ~ Soal ~

    Di dalam Java, Anda dapat melakukan kalkulasi lain seperti perkalian dan pembagian.
    Namun dengan simbol berbeda dari apa yang Anda gunakan dalam matematika. 
    Operator * untuk perkalian dan / untuk pembagian.
    Anda juga dapat menghitung modulus (hasil sisa pembagian) menggunakan operator %. 
*/
public class Main {
    public static void main(String[] args) {
        System.out.println(1 + 2);//Operator penambahan
        System.out.println(3 - 2);//Operator pengurangan
        System.out.println(4 * 3);//Operator perkalian
        System.out.println(5 / 4);//Operator pembagian
        System.out.println(3 % 6);//Operator Modulus/Sisah hasil bagi
    }
}
